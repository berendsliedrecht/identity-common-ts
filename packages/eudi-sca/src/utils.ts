export const Iso4217CurrencyCode = z.enum(ISO4217_CODES)

export const Iso4217Amount = z
  .string()
  .regex(/^\d+\.\d+ [A-Z]{3}$/)
  .refine((val) => ISO4217_CODES.includes(val.split(' ')[1] as (typeof ISO4217_CODES)[number]), {
    message: 'Invalid ISO4217 currency code',
  })

export const getJsonPathValueAndKey = (
  query: Array<string | null>,
  data: unknown
): { key: string; value: unknown } | undefined => {
  if (query.length === 0) return undefined
  if (typeof data !== 'object' || data === null) return undefined
  const [head, ...tail] = query
  const entries =
    head === null
      ? Object.entries(data as Record<string, unknown>)
      : ([[head, (data as Record<string, unknown>)[head]]] as [string, unknown][])

  for (const [key, child] of entries) {
    if (tail.length === 0) {
      if (child === undefined) return undefined
      return { key, value: child }
    }
    const result = getJsonPathValueAndKey(tail, child)
    if (result !== undefined) return result
  }

  return undefined
}
